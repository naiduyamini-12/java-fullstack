/**
 * This is a simple Java program to demonstrate:
 * 1. Single-line comment
 * 2. Multi-line comment
 * 3. Documentation comment
 */

public class CommentExample {

    public static void main(String[] args) {
        // This is a single-line comment
        System.out.println("Hello, this is a single-line comment example!");

        /*
         This is a multi-line comment.
         It can span across multiple lines.
        */
        System.out.println("This is a multi-line comment example.");

        /**
         * This is a documentation comment.
         * It is usually used to describe classes, methods, or fields.
         */
        System.out.println("This is a documentation comment example.");
    }
}
